a,b,c = input().split()
a=float(a)
b=float(b)
c=float(c)
print("TRIANGULO: {:.3f}".format(a*c/2),end='\n')
print("CIRCULO: {:.3f}".format(3.14159*c*c),end='\n')
print("TRAPEZIO: {:.3f}".format((a+b)*c/2),end='\n')
print("QUADRADO: {:.3f}".format(b*b),end='\n')
print("RETANGULO: {:.3f}".format(a*b),end='\n')